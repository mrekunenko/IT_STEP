#Користувач вводить 3 числа. Виведіть їхнє середнє арифметичне.

# Отримуємо три числа від користувача
chyslo1 = float(input("Введіть перше число: "))
chyslo2 = float(input("Введіть друге число: "))
chyslo3 = float(input("Введіть третє число: "))

# Обчислюємо суму чисел
suma = chyslo1 + chyslo2 + chyslo3

# Обчислюємо середнє арифметичне
serednye_aryfmetychne = suma / 3

# Виводимо результат
print(f"Середнє арифметичне: {serednye_aryfmetychne}")
