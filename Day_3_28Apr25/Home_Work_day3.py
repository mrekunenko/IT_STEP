# Завдання 2
# Щоб створити одну глиняну фігурку треба 130 грам глини.
# Користувач вводить масу глини в кілограмах.
# Виведіть скільки фігурок він зможе зробити.
# Наприклад, з 2 кг глини можна зробити 15 фігурок.

# Маса глини для однієї фігурки в грамах
masa_odnieyi_figurky = 130

# Отримуємо від користувача масу глини в кілограмах
masa_glyny_kg = float(input("Введіть масу глини в кілограмах: "))

# Переводимо масу глини в грами
masa_glyny_g = masa_glyny_kg * 1000

# Розраховуємо кількість можливих фігурок
kilkist_figurok = int(masa_glyny_g // masa_odnieyi_figurky)

# Виводимо результат
print(f"З {masa_glyny_kg} кг глини можна зробити {kilkist_figurok} фігурок")


# Завдання 3
# Користувач вводить радіус кола.
# Виведіть довжину кола. Вважайте що Pi=3.141593
# Округліть результат до трьох знаків після коми.

# Число пі
PI = 3.141593

# Отримуємо від користувача радіус кола
radius = float(input("Введіть радіус кола: "))

# Обчислюємо довжину кола за формулою 2*π*r
dovzhyna_kola = 2 * PI * radius

# Округлюємо результат до трьох знаків після коми
dovzhyna_kola_okruglena = round(dovzhyna_kola, 3)

# Виводимо результат
print(f"Довжина кола з радіусом {radius} дорівнює {dovzhyna_kola_okruglena}")


