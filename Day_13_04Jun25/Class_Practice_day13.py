# Завдання 3.
# a. товари – ["хліб", "молоко", "сир", "яблука", "кава"]
# b. ціни – [20.5, 28.0, 120.0, 45.3, 89.9]
# Користувач вводить назву товару, якщо такого товару
# немає, повідомити про це.
# Якщо товари є, то отримати його індекс та вивести
# відповідну ціну.

# Списки товарів та цін
products = ["хліб", "молоко", "сир", "яблука", "кава"]
prices = [20.5, 28.0, 120.0, 45.3, 89.9]

# Введення товару користувачем
product = input("Введіть назву товару: ").strip().lower()

# Перевірка наявності товару в списку
if product in products:
    index = products.index(product)
    price = prices[index]
    print(f"Ціна на {product} — {price} грн")
else:
    print("Такого товару немає в списку.")

