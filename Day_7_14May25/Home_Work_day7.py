# Завдання 2
# Користувач вводить з клавіатури два числа (початок та
# кінець діапазону). Проаналізуйте усі числа у цьому діапазоні.
# Виведіть на екран:
# 1. Усі числа діапазону.
# 2. Усі числа діапазону за спаданням.
# 3. Усі числа, кратні 7.
# 4. Кількість чисел, кратних 5.

# Введення діапазону
start = int(input("Введіть початок діапазону: "))
end = int(input("Введіть кінець діапазону: "))

# 1. Усі числа діапазону
print("1. Усі числа діапазону:")
for num in range(start, end + 1):
    print(num)

# 2. Усі числа діапазону за спаданням
print("2. Усі числа діапазону за спаданням:")
for num in range(end, start - 1, -1):
    print(num)

# 3. Усі числа, кратні 7
print("3. Числа, кратні 7:")
for num in range(start, end + 1):
    if num % 7 == 0:
        print(num)

# 4. Кількість чисел, кратних 5
count_5 = 0
for num in range(start, end + 1):
    if num % 5 == 0:
        count_5 += 1
print(f"4. Кількість чисел, кратних 5: {count_5}")


# Завдання 3
# Користувач вводить з клавіатури два числа (початок та кінець
# діапазону). Проаналізуйте усі числа у цьому діапазоні. Виведення на екран має відбуватися за правилами, наведеними нижче.
# Якщо число кратне 3 (ділиться на 3 без остачі), виведіть
# слово «Fizz». Якщо число кратне 5, виведіть слово «Buzz». Якщо
# число кратне 3 та 5, виведіть «Fizz Buzz». Якщо число не кратне
# ні 3, ані 5, виведіть тільки число.

# Введення діапазону
start = int(input("Введіть початок діапазону: "))
end = int(input("Введіть кінець діапазону: "))

# Аналіз чисел
for num in range(start, end + 1):
    if num % 3 == 0 and num % 5 == 0:
        print("Fizz Buzz")
    elif num % 3 == 0:
        print("Fizz")
    elif num % 5 == 0:
        print("Buzz")
    else:
        print(num)
