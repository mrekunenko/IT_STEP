# Завдання 1
# Користувач вводить ціну за одиницю товару та кількість
# товару. Якщо загальна сума більша за 500грн або купівля
# оптом(більше 10 шт), застосувати знижку 10%

# Отримання введених даних від користувача
tsina = float(input("Введіть ціну за одиницю товару (грн): "))
kilkist = int(input("Введіть кількість товару (шт): "))

# Розрахунок загальної суми
zahalna_suma = tsina * kilkist

# Перевірка умов для знижки
if zahalna_suma > 500 or kilkist > 10:
    # Застосовуємо знижку 10%
    znyzhka = zahalna_suma * 0.1
    suma_zi_znyzhkoyu = zahalna_suma - znyzhka
    print(f"Загальна сума: {zahalna_suma} грн")
    print(f"Застосована знижка 10%: {znyzhka} грн")
    print(f"Сума до сплати зі знижкою: {suma_zi_znyzhkoyu} грн")
else:
    # Знижка не застосовується
    print(f"Загальна сума: {zahalna_suma} грн")
    print("Знижка не застосовується.")


# Завдання 2
# Користувач вводить 2 оцінки студента.
# Якщо обидві більші за 60, то вивести «Склав»
# Якщо лише одна більша за 60 – вивести «Перездача»
# Якщо обидві менші за 60 – вивести «Не склав»
# Підказка: найскладнішу умову можна помістити в else

# Отримання введених оцінок від користувача
otsinka1 = float(input("Введіть першу оцінку студента: "))
otsinka2 = float(input("Введіть другу оцінку студента: "))

# Перевірка умов
if otsinka1 < 60 and otsinka2 < 60:
    print("Не склав")
elif otsinka1 > 60 and otsinka2 > 60:
    print("Склав")
else:
    print("Перездача")


# Завдання 3
# Користувач вводить рік, перевірте чи є він високосним.
# Високосний рік має ділитись на 4 та не ділитись на 100, але діляться на 400.
# Наприклад, 2024 – високосний, 2025 – не високосний(не ділиться на 4),
# 2100 – не високосний (ділиться на 100)

# Отримання року
rik = int(input("Введіть рік: "))

# Перевірка чи є рік високосним
if (rik % 4 == 0 and rik % 100 != 0) or (rik % 400 == 0):
    print("Рік є високосним")
else:
    print("Рік не є високосним")


# Завдання 4
# Користувач вводить суму кредиту та термін у роках.
# Виберіть відсоткову ставку за такими умовами:
# 1. Для кредиту до 10 000$ на термін до 3 років – ставка 8%
# 2. Для кредиту до 10 000$ на термін понад 3 роки – ставка 10%
# 3. Для кредиту від 10 001$ до 50 000$ на термін до 3 років – ставка 12%
# 4. Для кредиту від 10 001$ до 50 000$ на термін понад 3 роки – ставка 12%
# 5. Для кредиту від 50 000$ на будь-який термін – ставка 20%
# Виведіть відсоткову ставку, загальну суму кредиту та  щомісячний платіж
# Загальна сума кредиту вираховується так total = credit * (1 + percent / 100) ^ year

# Ввід суми кредиту та терміну
kredyt = float(input("Введіть суму кредиту ($): "))
termin = int(input("Введіть термін у роках: "))

# Визначення відсоткової ставки

if kredyt <= 10000:
    if termin <= 3:
        stavka = 8
    else:
        stavka = 10
elif 10001 <= kredyt <= 50000:
    if termin <= 3:
        stavka = 12
    else:
        stavka = 12  # Тут в завданні вказана та сама ставка 12%
else:
    stavka = 20

# Розрахунок загальної суми кредиту
total_suma = kredyt * (1 + stavka / 100) ** termin

# Розрахунок щомісячного платежу
kilkist_months = termin * 12
every_month_platizh = total_suma / kilkist_months

# Виведення результатів
print(f"Відсоткова ставка: {stavka}%")
print(f"Загальна сума кредиту: {total_suma:.2f} $")
print(f"Щомісячний платіж: {every_month_platizh:.2f} $")

