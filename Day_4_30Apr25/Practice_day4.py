print("1 - Євро (EUR)")
print("2 - Фунти (GBP)")
print("3 - Єни (JPY)")

# Отримуємо вибір користувача
vybir_valyuty = int(input("Ваш вибір (1-3): "))

# Залежно від вибору запитуємо курс відповідної валюти
if vybir_valyuty == 1: # Євро
    kurs = float(input("Введіть курс євро до долара: "))
    nazva_valyuty = "євро"
elif vybir_valyuty == 2:
    kurs = float(input("Введіть курс фунта до долара: "))
    nazva_valyuty = "фунтів"
elif vybir_valyuty == 3:  # Єни
    kurs = float(input("Введіть курс єни до долара: "))
    nazva_valyuty = "єн"
else:
    print("Виберіть число від 1 до 3.")

# Розраховуємо суму в обраній валюті
suma_v_inshiy_valyuti = suma_dolariv * kurs

# Виводимо результат
print(f"{suma_dolariv} USD = {suma_v_inshiy_valyuti} ({nazva_valyuty})")
