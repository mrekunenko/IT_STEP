# Завдання 5
# Користувач вводить з клавіатури два числа.
# Залежно від вибору користувача потрібно показати:
# суму двох чисел,
# різницю двох чисел,
# середньоарифметичне або
# добуток двох чисел.

# Отримуємо два числа від користувача
pershe_chyslo = float(input("Введіть перше число: "))
druge_chyslo = float(input("Введіть друге число: "))

# Виводимо меню вибору операції
print("Що треба зробити?")
print("1 - Сума двох чисел")
print("2 - Різниця двох чисел")
print("3 - Середньоарифметичне")
print("4 - Добуток двох чисел")

# Отримуємо вибір користувача
vybir = int(input("Ваш вибір (1-4): "))

# Виконуємо відповідну операцію залежно від вибору
if vybir == 1:
    rezultat = pershe_chyslo + druge_chyslo
    print(f"Сума: {rezultat}")
elif vybir == 2:
    rezultat = pershe_chyslo - druge_chyslo
    print(f"Різниця: {rezultat}")
elif vybir == 3:
    rezultat = (pershe_chyslo + druge_chyslo) / 2
    print(f"Середньоарифметичне: {rezultat}")
elif vybir == 4:
    rezultat = pershe_chyslo * druge_chyslo
    print(f"Добуток: {rezultat}")
else:
    print("Виберіть число від 1 до 4.")


# Завдання 6
# Користувач вводить із клавіатури суму в доларах,
# потім обирає валюту, у яку хоче перевести цю суму: євро (EUR), фунти (GBP) або єни (JPY).
# Після вибору валюти програма запитує курс обраної валюти по відношенню до долара.
# Програма повинна розрахувати і вивести суму в обраній валюті.

# Отримуємо суму в доларах від користувача
