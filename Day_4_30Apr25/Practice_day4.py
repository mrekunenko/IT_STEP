# Завдання 1
# Користувач вводить з клавіатури число. Необхідно перевірити його на парність і непарність.
# Якщо число парне потрібно вивести на екран число і напис "Even number".
# Якщо число непарне виведіть на екран число і напис "Odd number".

# Отримуємо число від користувача
num = int(input("Введіть ціле число: "))

# Перевіряємо число на парність
if num % 2 == 0:
    # Якщо число ділиться на 2 без залишку - воно парне
    print(f"{num} - Even number")
else:
    # Якщо є залишок - число непарне
    print(f"{num} - Odd number")


