# Завдання 1
# Користувач вводить з клавіатури три числа.
# Залежно від вибору користувача програма виводить на екран суму трьох чисел або добуток трьох чисел.

# Отримуємо три числа від користувача
chyslo_1 = float(input("Введіть перше число: "))
chyslo_2 = float(input("Введіть друге число: "))
chyslo_3 = float(input("Введіть третє число: "))

# Виводимо меню вибору операції
print("Оберіть операцію:")
print("1 - Сума трьох чисел")
print("2 - Добуток трьох чисел")

# Отримуємо вибір користувача
vybir = int(input("Ваш вибір (1 або 2): "))

# Виконуємо відповідну операцію залежно від вибору
if vybir == 1:
    rezultat = chyslo_1 + chyslo_2 + chyslo_3
    print(f"Сума чисел {chyslo_1}, {chyslo_2} та {chyslo_3} = {rezultat}")
elif vybir == 2:
    rezultat = chyslo_1 * chyslo_2 * chyslo_3
    print(f"Добуток чисел {chyslo_1}, {chyslo_2} та {chyslo_3} = {rezultat}")
else:
    print("Помилка! Виберіть 1 або 2.")


# Завдання 2
# Користувач вводить з клавіатури три числа.
# Залежно від вибору користувача програма виводить на екран максимум із трьох,
# мінімум із трьох або
# середньоарифметичне трьох чисел.

# Отримуємо три числа від користувача
chyslo_1 = float(input("Введіть перше число: "))
chyslo_2 = float(input("Введіть друге число: "))
chyslo_3 = float(input("Введіть третє число: "))

# Виводимо меню вибору операції
print("Оберіть операцію:")
print("1 - Максимум із трьох чисел")
print("2 - Мінімум із трьох чисел")
print("3 - Середньоарифметичне трьох чисел")

# Отримуємо вибір користувача
vybir = int(input("Ваш вибір (1-3): "))

# Виконуємо відповідну операцію залежно від вибору
if vybir == 1:
    # Знаходимо максимум
    max = chyslo_1
    if chyslo_2 > max:
        max = chyslo_2
    if chyslo_3 > max:
        max = chyslo_3
    print(f"Максимум із чисел {chyslo_1}, {chyslo_2} та {chyslo_3} = {max}")

elif vybir == 2:
    # Знаходимо мінімум
    minimum = chyslo_1
    if chyslo_2 < minimum:
        minimum = chyslo_2
    if chyslo_3 < minimum:
        minimum = chyslo_3
    print(f"Мінімум із чисел {chyslo_1}, {chyslo_2} та {chyslo_3} = {minimum}")

elif vybir == 3:
    # Обчислюємо середньоарифметичне
    serednye = (chyslo_1 + chyslo_2 + chyslo_3) / 3
    print(f"Середньоарифметичне чисел {chyslo_1}, {chyslo_2} та {chyslo_3} = {serednye}")

else:
    print("Помилка! Виберіть число від 1 до 3.")


