# Завдання 4
# Користувач вводить з клавіатури кількість метрів.
# Програма має запропонувати кілька варіантів перекладу і, залежно від вибору користувача,
# перевести метри в одну або кілька одиниць виміру.
# Можливі варіанти:
# 1. Перевести в одну з одиниць на вибір: милі, дюйми або ярди.
# 2. Перевести одразу в усі три одиниці (милі, дюйми та ярди) і вивести результати для кожної.
# 3. Перевести в кілометри та сантиметри, якщо користувач обирає цей варіант.

# Отримуємо кількість метрів від користувача
metry = float(input("Введіть кількість метрів: "))

# Виводимо меню вибору
print("Оберіть варіант перетворення:")
print("1 - Милі")
print("2 - Дюйми")
print("3 - Ярди")
print("4 - Усі три одиниці (милі, дюйми, ярди)")
print("5 - Кілометри та сантиметри")

# Отримуємо вибір
vybir = int(input("Ваш вибір (1-5): "))

# Виконуємо перетворення
if vybir == 1:
    print(f"{metry} метрів = {metry * 0.000621371} миль")
elif vybir == 2:
    print(f"{metry} метрів = {metry * 39.3701} дюймів")
elif vybir == 3:
    print(f"{metry} метрів = {metry * 1.09361} ярдів")
elif vybir == 4:
    print(f"{metry} метрів це:")
    print(f"a) {metry * 0.000621371} миль")
    print(f"b) {metry * 39.3701} дюймів")
    print(f"c) {metry * 1.09361} ярдів")
elif vybir == 5:
    print(f"{metry} метрів це:")
    print(f"a) {metry * 0.001} кілометрів")
    print(f"b) {metry * 100} сантиметрів")
else:
    print("Помилка! Виберіть число від 1 до 5.")
