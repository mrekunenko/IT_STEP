# Опис:
# 1. Спочатку, введіть імена гравців.
# 2. Кожен гравець по черзі обирає "Правда" або "Дія".
# 3. Якщо гравець обрав "Правда", йому задається запитання, на яке він повинен відповісти правдиво.
# 4. Якщо "Дія", гравець повинен виконати вказане завдання.
# 5. Після завершення кожного раунду, виведіть результати.
# Функції:
# 1. get_player_names(): Запитайте імена гравців і поверніть їх у вигляді списку.
# 2. ask_truth_or_dare(player): Функція, яка приймає ім'я гравця та запитує його, чи обирати "Правда" чи "Дія".
# 3. ask_truth_question(player): Функція, яка приймає ім'я гравця, виберє одне запитання випадковим чином та просить гравця дати відповідь.
# 4. perform_dare(player): Функція, яка приймає ім'я гравця та вибирає одне завдання випадковим чином та просить гравця виконаи його
# 5. play_game(players): Функція, яка керує ходом гри.
# Послідовно запитайте кожного гравця, чи обирати "Правда" чи "Дія", а потім відповідно викликайте функцію ask_truth_question або perform_dare.
# Константи:
# a. questions – список питань
# b. dares – список завдань

import random

# Константи - з допомогою ChatGPT!
questions = [
    "Який твій найбільший страх?",
    "Чи було щось, за що тобі досі соромно?",
    "Кому з присутніх ти найбільше довіряєш і чому?",
    "Чи закохувався(лась) ти у когось із друзів?",
    "Яка твоя найбільша таємниця, яку ти досі приховуєш?",
    "Чи коли-небудь ти говорив(ла) неправду, щоб уникнути покарання?",
    "Що найдивніше ти шукав(ла) в інтернеті?",
    "Чи був у тебе досвід, про який ніхто не знає?",
    "Який вчинок із твого минулого ти хотів(ла) би змінити?",
    "Якби можна було помінятися життям з кимось із присутніх — з ким і чому?"
]

dares = [
    "Заспівай пісню, яку знаєш, не соромлячись.",
    "Покажи пародію на вчителя / відомого блогера / політика.",
    "Зроби 10 присідань і скажи «Я крутий(а)» після кожного.",
    "Постав смішне фото на аватарку на 5 хвилин.",
    "Напиши випадковому контакту «Ти віриш у НЛО?».",
    "Скажи комплімент кожному гравцю.",
    "Зроби вигляд, що ти кішка, протягом 1 хвилини.",
    "Прочитай алфавіт навпаки якнайшвидше.",
    "Зателефонуй комусь і скажи, що виграв(ла) в лотерею.",
    "Танцюй під уявну музику 30 секунд."
]


def get_player_names():
    """Запитує імена гравців і повертає їх у вигляді списку"""
    players = []
    print("=== Гра 'Правда або Дія' ===")
    print("Введіть імена гравців (для завершення введіть 'стоп'):")

    while True:
        name = input("Ім'я гравця: ").strip()
        if name.lower() == 'стоп':
            break
        if name:
            players.append(name)
            print(f"Гравець {name} доданий!")

    return players


def ask_truth_or_dare(player):
    """Запитує гравця про вибір між 'Правда' та 'Дія'"""
    print(f"\n{player}, твоя черга!")
    choice = input("Обери 'Правда' або 'Дія' (п/д): ").strip().lower()

    if choice in ['п', 'правда']:
        return 'правда'
    elif choice in ['д', 'дія']:
        return 'дія'
    else:
        print("Неправильний вибір! Спробуй ще раз.")
        return ask_truth_or_dare(player)


def ask_truth_question(player):
    """Вибирає випадкове запитання для гравця"""
    question = random.choice(questions)
    print(f"\n{player}, твоє запитання:")
    print(f"'{question}'")

    answer = input("Твоя відповідь: ")
    print(f"Дякуємо за чесність, {player}!")
    return answer


def perform_dare(player):
    """Вибирає випадкове завдання для гравця"""
    dare = random.choice(dares)
    print(f"\n{player}, твоє завдання:")
    print(f"'{dare}'")

    input("Натисни Enter, коли виконаєш завдання...")
    print(f"Молодець, {player}! Завдання виконано!")


def play_game(players):
    """Керує ходом гри"""
    if len(players) < 2:
        print("Потрібно мінімум 2 гравці для гри!")
        return

    print(f"\n Починаємо гру з {len(players)} гравцями!")
    print("Гравці:", ", ".join(players))

    round_number = 1

    while True:
        print(f"\nРАУНД {round_number}")

        # Кожен гравець грає по черзі
        for player in players:
            choice = ask_truth_or_dare(player)

            if choice == 'правда':
                ask_truth_question(player)
            else:
                perform_dare(player)

            # Пауза між гравцями
            input("\nНатисни Enter для наступного гравця...")

        # Завершення раунду
        print(f"\n Раунд {round_number} завершено!")

        # Запитати про продовження гри
        continue_game = input("Хочете продовжити гру? (так/ні): ").strip().lower()
        if continue_game not in ['так', 'т', 'yes', 'y']:
            break

        round_number += 1

    print("\n Дякуємо за гру!")


# Основна функція для запуску гри
def main():
    """Головна функція запуску гри"""
    players = get_player_names()

    if players:
        play_game(players)
    else:
        print("Не введено жодного гравця. Гра завершена.")


# Запуск гри
if __name__ == "__main__":
    main()