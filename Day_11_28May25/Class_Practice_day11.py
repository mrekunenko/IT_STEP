# Завдання 1
# Користувач вводить рядок та індекс. Виведіть символ під
# цим індексом. Якщо індекс завеликий, виведіть інформацію
# про помилку.

text = input("Введіть рядок: ")
index = int(input("Введіть індекс: "))

if 0 <= index < len(text):
    print(f"Символ під індексом {index}: {text[index]}")
else:
    print("Помилка: індекс виходить за межі рядка")


# Завдання 2
# Користувач вводить рядок та два індекси. Виведіть зріз
# рядка.

text = input("Введіть рядок: ")
start = int(input("Введіть початковий індекс: "))
end = int(input("Введіть кінцевий індекс: "))

result = text[start:end]
print(f"Зріз рядка: '{result}'")


# Завдання 3
# Користувач вводить рядок та число n. Виведіть перші n
# символів. Також виведіть останні n символів.

text = input("Введіть рядок: ")
n = int(input("Введіть кількість символів n: "))

first_n = text[:n]
last_n = text[-n:]

print(f"Перші {n} символів: '{first_n}'")
print(f"Останні {n} символів: '{last_n}'")


